#!/usr/bin/env node
/**
 * ia-ocr-ingest.mjs — PLANET-2508
 *
 * Review-first, tiny-batch ingestion path for Internet Archive OCR/plaintext items.
 * Dry-run is default. Apply requires --apply plus --ack-reviewed and a reviewed
 * item list; requests stay serial and rate-limited.
 */

import { createHash } from 'node:crypto';
import { existsSync, readFileSync } from 'node:fs';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { createClient } from '@libsql/client';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const APP_ROOT = path.resolve(__dirname, '..');

const MIN_PASSAGE_CHARS = 180;
const TARGET_PASSAGE_CHARS = 300;
const MAX_PASSAGE_CHARS = 800;
const DEFAULT_REVIEWED_LIST = 'docs/ia-ocr-reviewed-items.json';
const DEFAULT_REPORT = 'docs/ia-ocr-ingest-report.md';
const DEFAULT_SAMPLE_JSON = 'docs/ia-ocr-ingest-samples.json';
const DEFAULT_CACHE = '.cache/ia-ocr-ingest';
const USER_AGENT = 'RandomPage/1.0 IA OCR ingest (tiny reviewed batch; contact gotexis+claw@gmail.com)';

const args = parseArgs(process.argv.slice(2));
const dryRun = !args.apply;
const apply = Boolean(args.apply);
const ackReviewed = Boolean(args['ack-reviewed']);
const maxItems = clampInt(args['max-items'], 1, 5, 2);
const maxPassagesPerItem = clampInt(args['max-passages-per-item'], 1, 25, 10);
const reviewedPath = path.resolve(APP_ROOT, args.reviewed || DEFAULT_REVIEWED_LIST);
const reportPath = path.resolve(APP_ROOT, args.report || DEFAULT_REPORT);
const samplesPath = path.resolve(APP_ROOT, args.samples || DEFAULT_SAMPLE_JSON);
const cacheDir = path.resolve(APP_ROOT, args.cache || DEFAULT_CACHE);

function parseArgs(argv) {
  const parsed = { refresh: false, json: false };
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (!token.startsWith('--')) continue;
    const key = token.slice(2);
    const next = argv[i + 1];
    if (!next || next.startsWith('--')) parsed[key] = true;
    else {
      parsed[key] = next;
      i += 1;
    }
  }
  return parsed;
}

function clampInt(value, min, max, fallback) {
  const n = Number(value);
  if (!Number.isFinite(n)) return fallback;
  return Math.max(min, Math.min(max, Math.floor(n)));
}

function loadEnvLocal() {
  const envPath = path.join(APP_ROOT, '.env.local');
  if (!existsSync(envPath)) return {};
  const text = readFileSync(envPath, 'utf8');
  const out = {};
  for (const rawLine of text.split('\n')) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#')) continue;
    const idx = line.indexOf('=');
    if (idx < 0) continue;
    const key = line.slice(0, idx).trim();
    let val = line.slice(idx + 1).trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) val = val.slice(1, -1);
    out[key] = val;
  }
  return out;
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function fetchText(url, { retries = 2 } = {}) {
  for (let attempt = 0; attempt <= retries; attempt += 1) {
    const res = await fetch(url, { headers: { 'User-Agent': USER_AGENT } });
    if (res.ok) return res.text();
    if ((res.status === 429 || res.status >= 500) && attempt < retries) {
      await sleep(1000 * (attempt + 1));
      continue;
    }
    throw new Error(`HTTP ${res.status} ${res.statusText}`);
  }
  throw new Error('unreachable fetch retry state');
}

async function fetchJson(url) {
  return JSON.parse(await fetchText(url));
}

async function cachedText(cacheName, url) {
  await mkdir(cacheDir, { recursive: true });
  const filePath = path.join(cacheDir, cacheName);
  if (!args.refresh && existsSync(filePath)) return readFile(filePath, 'utf8');
  const text = await fetchText(url);
  await writeFile(filePath, text);
  await sleep(500);
  return text;
}

function valueAsString(value) {
  if (Array.isArray(value)) return value.join('; ');
  return value == null ? '' : String(value);
}

function normalize(text) {
  return String(text || '').replace(/\s+/g, ' ').trim();
}

function cleanOcrText(raw) {
  return String(raw || '')
    .replace(/\r/g, '')
    .replace(/^\s*This is a digital copy of a book.*$/gim, '')
    .replace(/^\s*Generated by .*$/gim, '')
    .replace(/^\s*Digitized by .*$/gim, '')
    .replace(/^\s*Google\s*$/gim, '')
    .replace(/^\s*Internet Archive\s*$/gim, '')
    .replace(/^\s*The Library of Congress.*$/gim, '')
    .replace(/^\s*Copyright(ed)? material.*$/gim, '')
    .replace(/\f/g, '\n')
    .split('\n')
    .map((line) => line.replace(/[ \t]+/g, ' ').trim())
    .filter((line) => {
      if (!line) return true;
      if (/^\d+$/.test(line)) return false;
      if (/^[^A-Za-z]{1,12}$/.test(line)) return false;
      if (/^(chapter|section|book)\s+[ivxlcdm\d]+\.?$/i.test(line)) return true;
      if (line.length < 3) return false;
      return true;
    })
    .join('\n')
    .replace(/-\n(?=[a-z])/g, '')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

function isLikelyReferenceNoteFragment(text) {
  const normalized = normalize(text);
  if (!normalized) return false;
  if (normalized.startsWith('↩')) return true;
  if (/^(?:note|notes|footnote|footnotes|endnote|endnotes)\s*[:.\-—]/i.test(normalized)) return true;
  if (/^(?:\[[^\]]{1,80}\]|\([^)]{1,80}\))\s*(?:note|footnote|editor|translator|transcriber)/i.test(normalized)) return true;
  if (/^(?:for\s+.{1,80},\s*)?(?:see|cf\.)\s+(?:note|notes|footnote|footnotes|endnote|endnotes)\b/i.test(normalized)) return true;
  return ((normalized.slice(0, 220).match(/(?:↩|\[[0-9ivxlcdm]+\]|\([0-9ivxlcdm]+\)|\^[0-9]+|†|‡)/gi) ?? []).length >= 3);
}

function hasTerminalSentencePunctuation(text) {
  return /[.!?…。！？]["'”’）)\]》」』]*$/.test(normalize(text));
}

function rejectionReason(text) {
  const normalized = normalize(text);
  if (normalized.length < MIN_PASSAGE_CHARS) return 'too-short';
  if (normalized.length > MAX_PASSAGE_CHARS) return 'too-long';
  if (isLikelyReferenceNoteFragment(normalized)) return 'reference-note-or-footnote-fragment';
  if (isLikelyBoilerplateOrOcrNoise(normalized)) return 'boilerplate-or-ocr-noise';
  if (!hasTerminalSentencePunctuation(normalized)) return 'non-terminal-ending';
  const letters = (normalized.match(/[A-Za-z]/g) || []).length;
  if (letters / normalized.length <= 0.55) return 'low-letter-ratio';
  return null;
}

function isLikelyBoilerplateOrOcrNoise(text) {
  const normalized = normalize(text);
  const lower = normalized.toLowerCase();
  if (/\b(?:copyright|all rights reserved|first printing|second printing|publisher|publishers|printed in|library of congress|isbn|contents|table of contents)\b/i.test(normalized.slice(0, 260))) return true;
  const punctuation = (normalized.match(/[^\p{L}\p{N}\s]/gu) || []).length;
  if (punctuation / normalized.length > 0.18) return true;
  const weirdTokens = normalized.match(/[A-Za-z]*[0-9][A-Za-z]+|[A-Za-z]+[0-9][A-Za-z]*/g) || [];
  if (weirdTokens.length >= 4) return true;
  if (/^[^a-z]{20,}/i.test(normalized)) return true;
  if (lower.includes('greenberg, publisher') || lower.includes('w.w. norton') || lower.includes('charles e. merrill')) return true;
  return false;
}

function splitOnSentenceBoundaries(text) {
  const normalized = normalize(text);
  const matches = normalized.match(/[^.!?…。！？]+[.!?…。！？]["'”’）)\]》」』]*/g) || [];
  return matches.map((part) => part.trim()).filter(Boolean);
}

function sentenceBoundaryChunks(text) {
  const units = splitOnSentenceBoundaries(text).filter((unit) => unit.length <= MAX_PASSAGE_CHARS && !isLikelyReferenceNoteFragment(unit));
  const chunks = [];
  let buffer = '';
  for (const unit of units) {
    const next = buffer ? `${buffer} ${unit}` : unit;
    if (next.length <= MAX_PASSAGE_CHARS) {
      buffer = next;
      if (buffer.length >= TARGET_PASSAGE_CHARS) {
        if (!rejectionReason(buffer)) chunks.push(buffer);
        buffer = '';
      }
      continue;
    }
    if (!rejectionReason(buffer)) chunks.push(buffer);
    buffer = unit;
  }
  if (!rejectionReason(buffer)) chunks.push(buffer);
  return chunks;
}

function slicePassages(text, maxPassages = 500) {
  const paragraphs = text
    .split(/\n+/g)
    .map((p) => p.replace(/\s+/g, ' ').trim())
    .filter((p) => p.length > 0 && !isLikelyReferenceNoteFragment(p));
  const passages = [];
  let buffer = '';
  for (const paragraph of paragraphs) {
    const candidates = paragraph.length <= MAX_PASSAGE_CHARS ? [paragraph] : sentenceBoundaryChunks(paragraph);
    for (const candidate of candidates) {
      if (isLikelyReferenceNoteFragment(candidate)) continue;
      const next = buffer ? `${buffer}\n\n${candidate}` : candidate;
      if (next.length < TARGET_PASSAGE_CHARS) {
        buffer = next;
        continue;
      }
      if (!rejectionReason(next)) {
        passages.push(next);
        buffer = '';
      } else if (!rejectionReason(buffer)) {
        passages.push(buffer);
        buffer = candidate;
      } else {
        buffer = candidate;
      }
      if (passages.length >= maxPassages) break;
    }
    if (passages.length >= maxPassages) break;
  }
  if (passages.length < maxPassages && !rejectionReason(buffer)) passages.push(buffer);
  return passages.slice(0, maxPassages);
}

function findTextFile(metadata, reviewedFile) {
  const files = metadata.files || [];
  if (reviewedFile) {
    const exact = files.find((file) => file.name === reviewedFile);
    if (!exact) throw new Error(`reviewed textFile not found in IA metadata: ${reviewedFile}`);
    return exact;
  }
  const scored = files
    .filter((file) => file.name && /\.txt$/i.test(file.name))
    .map((file) => {
      let score = 0;
      if (/_djvu\.txt$/i.test(file.name)) score += 20;
      if (/(abbyy|ocr|text)/i.test(file.name)) score += 10;
      if (/meta|files|reviews/i.test(file.name)) score -= 50;
      score += Math.min(Number(file.size || 0) / 100000, 5);
      return { file, score };
    })
    .sort((a, b) => b.score - a.score);
  return scored[0]?.file || null;
}

function sha256Hex(text) {
  return createHash('sha256').update(text, 'utf8').digest('hex');
}

function normalizeTitle(value) {
  return normalize(value).toLowerCase().replace(/[\p{P}\p{S}]+/gu, ' ').replace(/\s+/g, ' ').trim();
}

function loadReviewedItems() {
  if (args.ids) {
    return String(args.ids).split(',').map((identifier) => ({ identifier: identifier.trim(), reviewed: true })).filter((item) => item.identifier).slice(0, maxItems);
  }
  if (!existsSync(reviewedPath)) throw new Error(`reviewed item list not found: ${reviewedPath}`);
  const parsed = JSON.parse(readFileSync(reviewedPath, 'utf8'));
  if (!Array.isArray(parsed)) throw new Error('reviewed item list must be a JSON array');
  return parsed.filter((item) => item?.identifier && item.reviewed !== false).slice(0, maxItems);
}

async function evaluateReviewedItem(item) {
  const metadata = await fetchJson(`https://archive.org/metadata/${encodeURIComponent(item.identifier)}`);
  const textFile = findTextFile(metadata, item.textFile);
  const title = String(item.title || valueAsString(metadata.metadata?.title) || item.identifier).trim();
  const author = String(item.author || valueAsString(metadata.metadata?.creator) || 'Unknown').trim();
  if (!textFile) throw new Error('no .txt OCR/plaintext file in IA metadata');
  const sourceUrl = `https://archive.org/download/${encodeURIComponent(item.identifier)}/${encodeURIComponent(textFile.name)}`;
  const raw = await cachedText(`${item.identifier}.txt`, sourceUrl);
  const clean = cleanOcrText(raw);
  // IA OCR front matter often contains cataloging, publisher, and contents pages.
  // The reviewed ingest path samples from the body after an initial offset so dry-run
  // rows are representative readable passages rather than title-page artifacts.
  const bodyText = clean.length > 12000 ? clean.slice(8000) : clean;
  const sliced = slicePassages(bodyText, maxPassagesPerItem * 3);
  const accepted = [];
  const rejected = {};
  for (const text of sliced) {
    const reason = rejectionReason(text);
    if (reason) rejected[reason] = (rejected[reason] || 0) + 1;
    else accepted.push(text);
    if (accepted.length >= maxPassagesPerItem) break;
  }
  return {
    identifier: item.identifier,
    title,
    author,
    file: textFile.name,
    sourceUrl,
    archiveUrl: `https://archive.org/details/${item.identifier}`,
    rawChars: raw.length,
    cleanChars: clean.length,
    acceptedPassages: accepted,
    rejected,
    status: accepted.length > 0 ? 'ready' : 'blocked',
    reason: accepted.length > 0 ? null : 'no accepted passages after length/content policy checks',
  };
}

async function existingDbState(client) {
  const [idsRes, titlesRes] = await Promise.all([
    client.execute('SELECT id FROM passages'),
    client.execute('SELECT DISTINCT book_title FROM passages'),
  ]);
  return {
    ids: new Set(idsRes.rows.map((r) => String(r.id))),
    titles: new Set(titlesRes.rows.map((r) => normalizeTitle(r.book_title))),
  };
}

function rowsForItem(itemResult, existingIds, seenIds) {
  const rows = [];
  const failures = [];
  for (const text of itemResult.acceptedPassages) {
    const id = sha256Hex(`${itemResult.identifier}\n${text}`).slice(0, 16);
    if (existingIds.has(id) || seenIds.has(id)) {
      failures.push({ id, reason: 'duplicate-id' });
      continue;
    }
    seenIds.add(id);
    rows.push({
      id,
      text,
      book_title: itemResult.title,
      author: itemResult.author,
      chapter: `Internet Archive OCR: ${itemResult.identifier}`,
      tags: '[]',
      language: 'en',
      sourceUrl: itemResult.sourceUrl,
      archiveUrl: itemResult.archiveUrl,
      charCount: text.length,
    });
  }
  return { rows, failures };
}

function markdownReport({ itemResults, rows, failures, inserted, blockedReason }) {
  const lines = [
    '# PLANET-2508 — IA OCR small-batch ingestion report',
    '',
    `Generated: ${new Date().toISOString()}`,
    '',
    '## Summary',
    '',
    `- Mode: ${apply ? 'apply' : 'dry-run'}`,
    `- Reviewed items evaluated: ${itemResults.length}`,
    `- Candidate rows accepted by policy: ${rows.length}`,
    `- Rows inserted: ${inserted}`,
    `- Failures/skips: ${failures.length}`,
    blockedReason ? `- Apply blocked: ${blockedReason}` : '- Apply blocked: no',
    '',
    '## Candidate rows',
    '',
    '| # | ID | Title | Author | Source URL | Chars | Preview |',
    '|---:|---|---|---|---|---:|---|',
  ];
  rows.slice(0, 50).forEach((row, idx) => {
    lines.push(`| ${idx + 1} | ${row.id} | ${escapeMd(row.book_title)} | ${escapeMd(row.author)} | ${row.sourceUrl} | ${row.charCount} | ${escapeMd(row.text.slice(0, 120))} |`);
  });
  lines.push('', '## Item-level results', '', '| Identifier | Status | Clean chars | Accepted | Rejected/skipped | Notes |', '|---|---|---:|---:|---|---|');
  for (const item of itemResults) {
    const failCount = failures.filter((f) => f.identifier === item.identifier).length;
    lines.push(`| ${item.identifier} | ${item.status} | ${item.cleanChars || 0} | ${item.acceptedPassages?.length || 0} | ${JSON.stringify(item.rejected || {})}${failCount ? ` + row skips ${failCount}` : ''} | ${escapeMd(item.reason || item.file || '')} |`);
  }
  lines.push('', '## Safety', '', '- Reviewed item list is explicit; no broad crawler/search is used.', '- Requests are serial with a descriptive User-Agent and small retry/backoff only for 429/5xx.', '- Passage rows must pass RandomPage bounds (180–800 chars), sentence-terminal ending, reference-note/footnote rejection, and letter-ratio checks before insert.', '- Inserted rows use `tags=[]`; existing tag cron handles later tagging.');
  return `${lines.join('\n')}\n`;
}

function escapeMd(value) {
  return String(value || '').replace(/\|/g, '\\|').replace(/\n/g, ' ').slice(0, 160);
}

async function main() {
  const reviewedItems = loadReviewedItems();
  if (reviewedItems.length === 0) throw new Error('no reviewed IA items supplied');
  if (apply && !ackReviewed) throw new Error('apply blocked: pass --ack-reviewed after confirming the reviewed item list and tiny batch');

  const itemResults = [];
  const failures = [];
  for (const item of reviewedItems) {
    try {
      const result = await evaluateReviewedItem(item);
      itemResults.push(result);
      process.stderr.write(`${result.status} ${result.identifier}: accepted=${result.acceptedPassages.length} cleanChars=${result.cleanChars}\n`);
    } catch (err) {
      const result = { identifier: item.identifier, status: 'blocked', acceptedPassages: [], rejected: {}, reason: err.message };
      itemResults.push(result);
      failures.push({ identifier: item.identifier, reason: err.message });
      process.stderr.write(`blocked ${item.identifier}: ${err.message}\n`);
    }
    await sleep(750);
  }

  const envLocal = loadEnvLocal();
  const TURSO_URL = process.env.TURSO_DATABASE_URL || envLocal.TURSO_DATABASE_URL;
  const TURSO_TOKEN = process.env.TURSO_AUTH_TOKEN || envLocal.TURSO_AUTH_TOKEN;
  let db = null;
  let existing = { ids: new Set(), titles: new Set() };
  let blockedReason = null;
  if (TURSO_URL && TURSO_TOKEN) {
    db = createClient({ url: TURSO_URL, authToken: TURSO_TOKEN });
    existing = await existingDbState(db);
  } else if (apply) {
    blockedReason = 'TURSO_DATABASE_URL and TURSO_AUTH_TOKEN are required for apply';
  }

  const seenIds = new Set();
  const rows = [];
  for (const result of itemResults.filter((r) => r.status === 'ready')) {
    if (existing.titles.has(normalizeTitle(result.title))) {
      failures.push({ identifier: result.identifier, reason: 'book-title-already-present' });
      continue;
    }
    const planned = rowsForItem(result, existing.ids, seenIds);
    rows.push(...planned.rows);
    for (const failure of planned.failures) failures.push({ identifier: result.identifier, ...failure });
  }

  let inserted = 0;
  if (apply && !blockedReason) {
    if (!db) blockedReason = 'database client unavailable';
    else if (rows.length === 0) blockedReason = 'no reviewed candidate rows passed policy/dedupe checks';
    else {
      const sql = 'INSERT INTO passages (id, text, book_title, author, chapter, tags, language) VALUES (?,?,?,?,?,?,?)';
      for (const row of rows) {
        try {
          await db.execute({ sql, args: [row.id, row.text, row.book_title, row.author, row.chapter, row.tags, row.language] });
          inserted += 1;
        } catch (err) {
          failures.push({ identifier: row.chapter, id: row.id, reason: `insert-failed: ${err.message}` });
        }
      }
    }
  }

  await mkdir(path.dirname(reportPath), { recursive: true });
  const report = markdownReport({ itemResults, rows, failures, inserted, blockedReason });
  await writeFile(reportPath, report);
  await writeFile(samplesPath, `${JSON.stringify({ mode: apply ? 'apply' : 'dry-run', rows, itemResults, failures, inserted, blockedReason }, null, 2)}\n`);

  if (db) await db.close?.();
  const summary = { mode: apply ? 'apply' : 'dry-run', reviewed_items: itemResults.length, candidate_rows: rows.length, inserted, failures: failures.length, blockedReason, report: path.relative(process.cwd(), reportPath), samples: path.relative(process.cwd(), samplesPath) };
  if (args.json) console.log(JSON.stringify(summary, null, 2));
  else console.log(`IA_OCR_INGEST mode=${summary.mode} reviewed_items=${summary.reviewed_items} candidate_rows=${summary.candidate_rows} inserted=${summary.inserted} failures=${summary.failures} report=${summary.report}`);
  if (apply && blockedReason) process.exitCode = 1;
}

main().catch((err) => {
  console.error(err.stack || err.message);
  process.exit(1);
});
