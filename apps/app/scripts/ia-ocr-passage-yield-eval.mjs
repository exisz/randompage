#!/usr/bin/env node
/**
 * ia-ocr-passage-yield-eval.mjs — PLANET-3696
 *
 * Reviewed IA/OCR passage-yield evaluation. This is an evaluation-only CLI:
 * it reads an explicit human allowlist, fetches IA metadata + OCR/plaintext for
 * rows marked reviewed=true and allowOcrFetch=true, slices local candidate
 * passages, and writes report artifacts only. It never writes production data.
 */

import { existsSync, readFileSync } from 'node:fs';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const APP_ROOT = path.resolve(__dirname, '..');

const MIN_PASSAGE_CHARS = 180;
const TARGET_PASSAGE_CHARS = 300;
const MAX_PASSAGE_CHARS = 800;
const DEFAULT_REVIEWED = 'docs/ia-ocr-passage-yield-reviewed-fixture.json';
const DEFAULT_REPORT = 'docs/ia-ocr-passage-yield-report.md';
const DEFAULT_SAMPLES = 'docs/ia-ocr-passage-yield-samples.json';
const DEFAULT_CACHE = '.cache/ia-ocr-passage-yield';
const USER_AGENT = 'RandomPage/1.0 reviewed IA OCR passage-yield eval (contact gotexis+claw@gmail.com)';

const args = parseArgs(process.argv.slice(2));
const reviewedPath = path.resolve(APP_ROOT, args.reviewed || DEFAULT_REVIEWED);
const reportPath = path.resolve(APP_ROOT, args.report || DEFAULT_REPORT);
const samplesPath = path.resolve(APP_ROOT, args.samples || DEFAULT_SAMPLES);
const cacheDir = path.resolve(APP_ROOT, args.cache || DEFAULT_CACHE);
const maxItems = clampInt(args['max-items'], 1, 25, 10);
const maxSamplesPerItem = clampInt(args['samples-per-item'], 1, 5, 3);

function parseArgs(argv) {
  const parsed = { refresh: false, json: false };
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (!token.startsWith('--')) continue;
    const key = token.slice(2);
    const next = argv[i + 1];
    if (!next || next.startsWith('--')) parsed[key] = true;
    else {
      parsed[key] = next;
      i += 1;
    }
  }
  return parsed;
}

function clampInt(value, min, max, fallback) {
  const n = Number(value);
  if (!Number.isFinite(n)) return fallback;
  return Math.max(min, Math.min(max, Math.floor(n)));
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function fetchText(url, { retries = 2 } = {}) {
  for (let attempt = 0; attempt <= retries; attempt += 1) {
    const res = await fetch(url, { headers: { 'User-Agent': USER_AGENT } });
    if (res.ok) return res.text();
    if ((res.status === 429 || res.status >= 500) && attempt < retries) {
      await sleep(1000 * (attempt + 1));
      continue;
    }
    throw new Error(`HTTP ${res.status} ${res.statusText}`);
  }
  throw new Error('unreachable fetch retry state');
}

async function fetchJson(url) {
  return JSON.parse(await fetchText(url));
}

async function cachedText(cacheName, url) {
  await mkdir(cacheDir, { recursive: true });
  const filePath = path.join(cacheDir, cacheName);
  if (!args.refresh && existsSync(filePath)) return readFile(filePath, 'utf8');
  const text = await fetchText(url);
  await writeFile(filePath, text);
  await sleep(500);
  return text;
}

function normalize(text) {
  return String(text || '').replace(/\s+/g, ' ').trim();
}

function valueAsString(value) {
  if (Array.isArray(value)) return value.join('; ');
  return value == null ? '' : String(value);
}

function loadReviewedItems() {
  if (!existsSync(reviewedPath)) throw new Error(`reviewed allowlist not found: ${reviewedPath}`);
  const parsed = JSON.parse(readFileSync(reviewedPath, 'utf8'));
  if (!Array.isArray(parsed)) throw new Error('reviewed allowlist must be a JSON array');
  return parsed
    .filter((item) => item?.identifier)
    .map((item) => ({ ...item, allowOcrFetch: item.allowOcrFetch === true || item.allowocrfetch === true || item.allow_ocr_fetch === true }))
    .filter((item) => item.reviewed === true && item.allowOcrFetch === true)
    .slice(0, maxItems);
}

function cleanOcrText(raw) {
  return String(raw || '')
    .replace(/\r/g, '')
    .replace(/^\s*This is a digital copy of a book.*$/gim, '')
    .replace(/^\s*Generated by .*$/gim, '')
    .replace(/^\s*Digitized by .*$/gim, '')
    .replace(/^\s*Google\s*$/gim, '')
    .replace(/^\s*Internet Archive\s*$/gim, '')
    .replace(/^\s*The Library of Congress.*$/gim, '')
    .replace(/^\s*Copyright(ed)? material.*$/gim, '')
    .replace(/\f/g, '\n')
    .split('\n')
    .map((line) => line.replace(/[ \t]+/g, ' ').trim())
    .filter((line) => {
      if (!line) return true;
      if (/^\d+$/.test(line)) return false;
      if (/^[^A-Za-z]{1,12}$/.test(line)) return false;
      if (/^(chapter|section|book)\s+[ivxlcdm\d]+\.?$/i.test(line)) return true;
      if (line.length < 3) return false;
      return true;
    })
    .join('\n')
    .replace(/-\n(?=[a-z])/g, '')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

function isLikelyReferenceNoteFragment(text) {
  const normalized = normalize(text);
  if (!normalized) return false;
  if (normalized.startsWith('↩')) return true;
  if (/^(?:note|notes|footnote|footnotes|endnote|endnotes)\s*[:.\-—]/i.test(normalized)) return true;
  if (/^(?:\[[^\]]{1,80}\]|\([^)]{1,80}\))\s*(?:note|footnote|editor|translator|transcriber)/i.test(normalized)) return true;
  if (/^(?:for\s+.{1,80},\s*)?(?:see|cf\.)\s+(?:note|notes|footnote|footnotes|endnote|endnotes)\b/i.test(normalized)) return true;
  return ((normalized.slice(0, 220).match(/(?:↩|\[[0-9ivxlcdm]+\]|\([0-9ivxlcdm]+\)|\^[0-9]+|†|‡)/gi) ?? []).length >= 3);
}

function isLikelyChapterListFragment(text) {
  const normalized = normalize(text);
  if (!normalized) return false;
  const chapterMatches = normalized.match(/(?:^|[\s.;:!?。！？])(?:chapter|chap\.|book|part|section)\s+(?:[0-9ivxlcdm]+|[a-z][a-z'’-]{1,30})(?=[\s.:;,-])/gi) ?? [];
  if (chapterMatches.length < 4) return false;
  const proseWords = normalized.match(/\b(?:the|and|but|for|with|from|that|this|they|their|there|then|when|where|while|into|upon|because|said|was|were|had|have|will|would|could|should|not)\b/gi) ?? [];
  const proseRatio = proseWords.length / Math.max(1, normalized.split(/\s+/).length);
  return chapterMatches.length >= 6 || proseRatio < 0.18;
}

function isLikelyBoilerplateOrOcrNoise(text) {
  const normalized = normalize(text);
  const lower = normalized.toLowerCase();
  if (/\b(?:copyright|all rights reserved|first printing|second printing|publisher|publishers|printed in|library of congress|isbn|contents|table of contents)\b/i.test(normalized.slice(0, 260))) return true;
  const punctuation = (normalized.match(/[^\p{L}\p{N}\s]/gu) || []).length;
  if (punctuation / normalized.length > 0.18) return true;
  const weirdTokens = normalized.match(/[A-Za-z]*[0-9][A-Za-z]+|[A-Za-z]+[0-9][A-Za-z]*/g) || [];
  if (weirdTokens.length >= 4) return true;
  if (/^[^a-z]{20,}/i.test(normalized)) return true;
  if (lower.includes('greenberg, publisher') || lower.includes('w.w. norton') || lower.includes('charles e. merrill')) return true;
  return false;
}

function hasTerminalSentencePunctuation(text) {
  return /[.!?…。！？]["'”’）)\]》」』]*$/.test(normalize(text));
}

function rejectionReason(text) {
  const normalized = normalize(text);
  if (normalized.length < MIN_PASSAGE_CHARS) return 'too-short';
  if (normalized.length > MAX_PASSAGE_CHARS) return 'too-long';
  if (isLikelyReferenceNoteFragment(normalized)) return 'reference-note-or-footnote-fragment';
  if (isLikelyChapterListFragment(normalized)) return 'chapter-list-fragment';
  if (isLikelyBoilerplateOrOcrNoise(normalized)) return 'boilerplate-or-ocr-noise';
  if (!hasTerminalSentencePunctuation(normalized)) return 'non-terminal-ending';
  const letters = (normalized.match(/[A-Za-z]/g) || []).length;
  if (letters / normalized.length <= 0.55) return 'low-letter-ratio';
  return null;
}

function splitOnSentenceBoundaries(text) {
  const matches = normalize(text).match(/[^.!?…。！？]+[.!?…。！？]["'”’）)\]》」』]*/g) || [];
  return matches.map((part) => part.trim()).filter(Boolean);
}

function sentenceBoundaryChunks(text) {
  const units = splitOnSentenceBoundaries(text).filter((unit) => unit.length <= MAX_PASSAGE_CHARS && !isLikelyReferenceNoteFragment(unit) && !isLikelyChapterListFragment(unit));
  const chunks = [];
  let buffer = '';
  for (const unit of units) {
    const next = buffer ? `${buffer} ${unit}` : unit;
    if (next.length <= MAX_PASSAGE_CHARS) {
      buffer = next;
      if (buffer.length >= TARGET_PASSAGE_CHARS) {
        if (!rejectionReason(buffer)) chunks.push(buffer);
        buffer = '';
      }
      continue;
    }
    if (!rejectionReason(buffer)) chunks.push(buffer);
    buffer = unit;
  }
  if (!rejectionReason(buffer)) chunks.push(buffer);
  return chunks;
}

function slicePassages(text, maxPassages = 500) {
  const paragraphs = text
    .split(/\n+/g)
    .map((p) => p.replace(/\s+/g, ' ').trim())
    .filter((p) => p.length > 0 && !isLikelyReferenceNoteFragment(p) && !isLikelyChapterListFragment(p));
  const passages = [];
  let buffer = '';
  for (const paragraph of paragraphs) {
    const candidates = paragraph.length <= MAX_PASSAGE_CHARS ? [paragraph] : sentenceBoundaryChunks(paragraph);
    for (const candidate of candidates) {
      if (isLikelyReferenceNoteFragment(candidate) || isLikelyChapterListFragment(candidate)) continue;
      const next = buffer ? `${buffer}\n\n${candidate}` : candidate;
      if (next.length < TARGET_PASSAGE_CHARS) {
        buffer = next;
        continue;
      }
      if (!rejectionReason(next)) {
        passages.push(next);
        buffer = '';
      } else if (!rejectionReason(buffer)) {
        passages.push(buffer);
        buffer = candidate;
      } else {
        buffer = candidate;
      }
      if (passages.length >= maxPassages) break;
    }
    if (passages.length >= maxPassages) break;
  }
  if (passages.length < maxPassages && !rejectionReason(buffer)) passages.push(buffer);
  return passages.slice(0, maxPassages);
}

function findTextFile(metadata, reviewedFile) {
  const files = metadata.files || [];
  if (reviewedFile) {
    const exact = files.find((file) => file.name === reviewedFile);
    if (!exact) throw new Error(`reviewed textFile not found in IA metadata: ${reviewedFile}`);
    return exact;
  }
  const scored = files
    .filter((file) => file.name && /\.txt$/i.test(file.name))
    .map((file) => {
      let score = 0;
      if (/_djvu\.txt$/i.test(file.name)) score += 20;
      if (/(abbyy|ocr|text)/i.test(file.name)) score += 10;
      if (/meta|files|reviews/i.test(file.name)) score -= 50;
      score += Math.min(Number(file.size || 0) / 100000, 5);
      return { file, score };
    })
    .sort((a, b) => b.score - a.score);
  return scored[0]?.file || null;
}

function metadataPageCount(metadata) {
  const candidates = [metadata.metadata?.pages, metadata.metadata?.imagecount, metadata.metadata?.pagecount];
  for (const candidate of candidates) {
    const n = Number(Array.isArray(candidate) ? candidate[0] : candidate);
    if (Number.isFinite(n) && n > 0) return n;
  }
  return null;
}

async function evaluateItem(item) {
  const metadataUrl = `https://archive.org/metadata/${encodeURIComponent(item.identifier)}`;
  const metadata = await fetchJson(metadataUrl);
  const title = String(item.title || valueAsString(metadata.metadata?.title) || item.identifier).trim();
  const author = String(item.author || valueAsString(metadata.metadata?.creator) || 'Unknown').trim();
  const textFile = findTextFile(metadata, item.textFile);
  const base = {
    identifier: item.identifier,
    title,
    author,
    topic: item.topic || null,
    archiveUrl: `https://archive.org/details/${item.identifier}`,
    metadataUrl,
    pageCount: metadataPageCount(metadata),
  };
  if (!textFile) return { ...base, status: 'metadata-only', failureReason: 'IA metadata has no OCR/plaintext .txt file', httpStatus: 200, textFile: null, rawChars: 0, cleanChars: 0, passageCount: 0, samplePassages: [] };

  const fileUrl = `https://archive.org/download/${encodeURIComponent(item.identifier)}/${encodeURIComponent(textFile.name)}`;
  const raw = await cachedText(`${item.identifier}.txt`, fileUrl);
  const clean = cleanOcrText(raw);
  const bodyText = clean.length > 12000 ? clean.slice(8000) : clean;
  const passages = slicePassages(bodyText);
  return {
    ...base,
    status: passages.length > 0 ? 'ocr-plaintext-usable' : 'search-inside-snippet-only',
    failureReason: passages.length > 0 ? null : 'text fetched but no clean RandomPage-sized passage passed filters',
    fetchMethod: textFile.name.endsWith('_djvu.txt') ? 'ia-djvu-ocr-text' : 'ia-plaintext-text',
    httpStatus: 200,
    textFile: textFile.name,
    textUrl: fileUrl,
    rawChars: raw.length,
    cleanChars: clean.length,
    passageCount: passages.length,
    avgPassageChars: passages.length ? Math.round(passages.reduce((sum, p) => sum + p.length, 0) / passages.length) : 0,
    samplePassages: passages.slice(0, maxSamplesPerItem),
  };
}

function summarize(results) {
  const byStatus = Object.fromEntries(['metadata-only', 'search-inside-snippet-only', 'ocr-plaintext-usable', 'failed-blocked'].map((status) => [status, results.filter((r) => r.status === status).length]));
  const usable = results.filter((r) => r.status === 'ocr-plaintext-usable');
  const totalPassages = usable.reduce((sum, r) => sum + r.passageCount, 0);
  return {
    totalCandidates: results.length,
    fetchedTextCount: usable.length + byStatus['search-inside-snippet-only'],
    cleanPassageCount: totalPassages,
    averagePassagesPerUsableBook: usable.length ? Number((totalPassages / usable.length).toFixed(1)) : 0,
    statusCounts: byStatus,
    topFailureModes: Object.entries(results.reduce((acc, r) => {
      if (r.failureReason) acc[r.failureReason] = (acc[r.failureReason] || 0) + 1;
      return acc;
    }, {})).sort((a, b) => b[1] - a[1]).map(([reason, count]) => ({ reason, count })),
  };
}

function escapeMd(value, length = 180) {
  return String(value || '').replace(/\|/g, '\\|').replace(/\n/g, ' ').slice(0, length);
}

function markdownReport(results, summary) {
  const rows = results.map((r, i) => `| ${i + 1} | ${r.status} | ${escapeMd(r.identifier)} | ${escapeMd(r.title)} | ${escapeMd(r.author)} | ${r.httpStatus || 'n/a'} | ${r.pageCount || ''} | ${r.rawChars || 0} | ${r.cleanChars || 0} | ${r.passageCount || 0} | ${escapeMd(r.failureReason || r.textFile || '')} |`).join('\n');
  const failureRows = summary.topFailureModes.map((f) => `| ${escapeMd(f.reason)} | ${f.count} |`).join('\n');
  const sampleSections = results
    .filter((r) => r.samplePassages?.length)
    .slice(0, 5)
    .map((r) => [`### ${escapeMd(r.title)} — ${escapeMd(r.author)}`, '', `IA: ${r.archiveUrl}`, '', ...r.samplePassages.slice(0, maxSamplesPerItem).flatMap((sample) => [`> ${sample.replace(/\n+/g, ' ')}`, ''])].join('\n'))
    .join('\n');
  const verdict = summary.statusCounts['ocr-plaintext-usable'] >= 5
    ? 'At least 5 reviewed items produced clean candidate passages; IA/OCR is worth a guarded follow-up ingest batch.'
    : 'Fewer than 5 reviewed items produced clean candidate passages; expand/improve the reviewed allowlist before ingesting.';

  return `# PLANET-3696 — Reviewed IA/OCR passage-yield evaluation

Generated: ${new Date().toISOString()}
Reviewed allowlist: \`${path.relative(APP_ROOT, reviewedPath)}\`

## Command

\`\`\`bash
pnpm --filter @randompage/app eval:ia-ocr-passage-yield -- --max-items ${maxItems}
\`\`\`

## Summary

- Total reviewed candidates: ${summary.totalCandidates}
- Text fetched count: ${summary.fetchedTextCount}
- Clean candidate passages: ${summary.cleanPassageCount}
- Average passages / usable book: ${summary.averagePassagesPerUsableBook}
- Metadata-only: ${summary.statusCounts['metadata-only']}
- Search-inside/snippet-only or unusable fetched text: ${summary.statusCounts['search-inside-snippet-only']}
- OCR/plaintext usable: ${summary.statusCounts['ocr-plaintext-usable']}
- Failed/blocked: ${summary.statusCounts['failed-blocked']}

## Verdict

${verdict}

## Top failure modes

| Failure mode | Count |
|---|---:|
${failureRows || '| none | 0 |'}

## Item results

| # | Classification | IA identifier | Title | Author | HTTP | Pages | Raw chars | Clean chars | Passages | Notes |
|---:|---|---|---|---|---:|---:|---:|---:|---:|---|
${rows || '| — | — | — | — | — | — | — | 0 | 0 | 0 | — |'}

## Safety boundary

- Only rows with \`reviewed=true\` and \`allowOcrFetch=true\` are fetched.
- This CLI writes local report/cache artifacts only; no Turso client, no mutation SQL, and no Discover/random sampling change.
- Candidate passages reuse RandomPage length/content filters and remain review artifacts until a separate approved ingest path is run.

## Sample passages for human review

${sampleSections || 'No usable samples produced.'}
`;
}

async function main() {
  const allowlist = loadReviewedItems();
  if (allowlist.length === 0) throw new Error('no reviewed=true + allowOcrFetch=true items supplied');

  const results = [];
  for (const item of allowlist) {
    try {
      const result = await evaluateItem(item);
      results.push(result);
      process.stderr.write(`${result.status} ${result.identifier}: passages=${result.passageCount || 0}\n`);
    } catch (err) {
      results.push({ identifier: item.identifier, title: item.title || item.identifier, author: item.author || 'Unknown', topic: item.topic || null, status: 'failed-blocked', failureReason: err.message, httpStatus: null, rawChars: 0, cleanChars: 0, passageCount: 0, samplePassages: [] });
      process.stderr.write(`failed-blocked ${item.identifier}: ${err.message}\n`);
    }
    await sleep(750);
  }

  const summary = summarize(results);
  await mkdir(path.dirname(reportPath), { recursive: true });
  await writeFile(reportPath, markdownReport(results, summary));
  await writeFile(samplesPath, `${JSON.stringify({ generatedAt: new Date().toISOString(), reviewed: path.relative(APP_ROOT, reviewedPath), summary, results }, null, 2)}\n`);

  const payload = { ...summary, report: path.relative(process.cwd(), reportPath), samples: path.relative(process.cwd(), samplesPath) };
  if (args.json) console.log(JSON.stringify(payload, null, 2));
  else console.log(`IA_OCR_PASSAGE_YIELD total=${payload.totalCandidates} fetched=${payload.fetchedTextCount} usable=${payload.statusCounts['ocr-plaintext-usable']} passages=${payload.cleanPassageCount} report=${payload.report}`);
}

main().catch((err) => {
  console.error(err.stack || err.message);
  process.exit(1);
});
